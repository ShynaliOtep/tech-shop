    2.1. Передача оборудования в пользования Арендатора и возврат его Арендодателю осуществляется по акту приему-передач
    оборудования, подписанному уполномоченными представителями сторон.
    <br>
    2.2. Доставка оборудования Арендатору до его местонахождения и возврат оборудования Арендодателю, осуществляется
    Арендатором собственными силами и за свой счет.
    <br>
    2.3. Днем исполнения Арендодателем по передачи оборудования считается днем подписания Акта приема-передач.
