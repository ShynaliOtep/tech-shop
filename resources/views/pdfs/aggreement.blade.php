    <br>
    3.1.2. Регулярно и в соответствии с условиями договора производить выплату арендных платежей.
    <br>
    3.1.3. Поддерживать в течение срока настоящего договора в исправном техническом состоянии и осуществлять за свой
    счет необходимые для эксплуатации согласования (разрешения, сертификация), текущий ремонт.
    <br>
    3.1.4. По истечении срока договора аренды вернуть Оборудования, в технически исправном состоянии.
    <br>
    3.1.5. в случае поломки, полной утраты, утери, невозврата оборудования Арендатор обязан в течение 1 (Одного)
    календарного дня предоставить новое аналогичное оборудование, либо выплатить Арендодателю <b>стоимость
