    оплаты задолженности по договору.
    <br>
    3.1.6. Осуществлять за свой счет хранение и охрану оборудования.
    <br>
    3.1.7. Не отдавать Оборудование, в субаренду, Ломбард без письменного разрешения Арендодателя. Соблюдать все
    инструкции по уходу, техническому обслуживание и эксплуатации оборудования.
    <br>

    <p class="center-align"><b>3.2. Арендодатель обязуется:</b></p>
    <br>
    3.2.1. Обеспечить Оборудования, полным комплектом оборудования
    <br>
    3.2.2. Принять Оборудования по истечении срока договора аренды.
    <p class="center-align"><b>4. Ответственность сторон</b></p>
    4.1. Арендатор обязан использовать полученное в аренду оборудование в соответствие с условиями настоящего договора и
    исключительно по прямому производственному и потребительскому назначению названного оборудования.
    <br>
    4.2. Арендатор несет возникающие в связи с эксплуатацией арендованного оборудования расходы, в том числе оплату
    текущего ремонта и расходуемых в процессе эксплуатации материалов, поддерживает оборудование в исправном, чистом
    состоянии.
    <br>
    4.3. В случае нарушения сроков возврата оборудования по истечение срока аренды Арендатором. Срок аренды оборудования
    считается продленным по инициативе Арендатора и уплачивается Арендатором согласно установленными настоящим договором
    ценами и условиями. Минимальный срок продления 1 (одни) сутки.
    <br>
    4.4. В случае неуплаты задолженности по настоящему договору Арендатором на момент возврата оборудования Арендодатель
    имеет право не принимать оборудование. В этом случае срок аренды считается продленным и уплачивается Арендатором
    согласно установленным настоящим договором ценами и условиями. Минимальный срок продления 1 (Одни) сутки.
    <br>
    4.5. Арендатор несет ответственность за полную утрату Имущества после его принятия по акту приема-передачи.
    <br>
    4.6. За причинение вреда третьим лицам оборудованием (оборудованиям), ответственность несет Арендатор в соответствии
    с действующим законодательством.
    <br>
    4.7. За просрочку выплаты платежей, указанных в настоящем договоре, Арендодатель вправе требовать от Арендатора
