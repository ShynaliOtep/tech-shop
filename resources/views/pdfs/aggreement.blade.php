    <br>
    4.8. Право пользованием оборудованием возникает у Арендатора после передачи ему оборудования по акту-приему передач.
    С этого момента риск случайной гибели, порчи, утраты, утери оборудования лежит на Арендаторе.
    <br>
    4.9. Арендатор несет полную ответственность за выбор того или иного оборудования.
